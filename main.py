@tool
def find_recipe_by_ingredients(ingredients: str) -> str:
    """Найти рецепты по списку продуктов из холодильника. Принимает продукты через запятую."""
    db = {
        "курица": [
            "Куриный суп с лапшой — курица, морковь, лук, лапша. Время: 40 мин.",
            "Запечённая курица с чесноком — курица, чеснок, оливковое масло. Время: 60 мин.",
            "Куриное рагу — курица, картофель, перец, томаты. Время: 50 мин.",
        ],
        "гречка": [
            "Гречка с грибами — гречка, шампиньоны, лук. Время: 30 мин.",
            "Гречневая каша с маслом — гречка, сливочное масло. Время: 20 мин.",
            "Гречка по-купечески — гречка, свинина, морковь, лук. Время: 45 мин.",
        ],
        "яйца": [
            "Омлет с сыром — яйца, молоко, сыр. Время: 10 мин.",
            "Яичница с помидорами — яйца, томаты, зелень. Время: 8 мин.",
            "Фриттата с овощами — яйца, болгарский перец, лук. Время: 20 мин.",
        ],
        "картофель": [
            "Жареная картошка с луком — картофель, лук, масло. Время: 25 мин.",
            "Картофельный суп-пюре — картофель, сливки, лук. Время: 35 мин.",
            "Запечённый картофель — картофель, специи, оливковое масло. Время: 45 мин.",
        ],
    }
    items = [i.strip().lower() for i in ingredients.split(",")]
    results = []
    for item in items:
        for key, recipes in db.items():
            if key in item or item in key:
                results.extend(recipes)
    if not results:
        results = [
            f"Салат из того, что есть — {ingredients}, соль, масло. Время: 10 мин.",
            f"Суп с {ingredients} — бульон, лук, морковь. Время: 30 мин.",
            f"Запеканка с {ingredients} — яйца, молоко, сыр. Время: 40 мин.",
        ]
    return "Найденные рецепты:\n" + "\n".join(f"• {r}" for r in results[:3])


