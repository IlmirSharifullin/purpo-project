# --- Nutritionist ---

@tool
def calculate_nutrition(meal: str, grams: float) -> str:
    """Рассчитать КБЖУ (калории, белки, жиры, углеводы) для блюда. meal — название блюда, grams — количество в граммах."""
    # КБЖУ на 100г
    db = {
        "куриная грудка": {"kcal": 165, "protein": 31, "fat": 3.6, "carbs": 0},
        "гречка": {"kcal": 343, "protein": 13, "fat": 3.4, "carbs": 68},
        "яйцо": {"kcal": 155, "protein": 13, "fat": 11, "carbs": 1.1},
        "омлет": {"kcal": 130, "protein": 10, "fat": 9, "carbs": 2},
        "картофель": {"kcal": 77, "protein": 2, "fat": 0.1, "carbs": 17},
        "овсянка": {"kcal": 68, "protein": 2.4, "fat": 1.4, "carbs": 12},
        "рис": {"kcal": 344, "protein": 6.7, "fat": 0.7, "carbs": 78},
        "творог": {"kcal": 121, "protein": 18, "fat": 5, "carbs": 3},
    }
    ratio = grams / 100
    key = meal.lower()
    for k, v in db.items():
        if k in key or key in k:
            return (
                f"КБЖУ для {grams}г «{meal}»:\n"
                f"• Калории:  {v['kcal'] * ratio:.0f} ккал\n"
                f"• Белки:    {v['protein'] * ratio:.1f} г\n"
                f"• Жиры:     {v['fat'] * ratio:.1f} г\n"
                f"• Углеводы: {v['carbs'] * ratio:.1f} г"
            )
    return (
        f"КБЖУ для {grams}г «{meal}» (приблизительно):\n"
        f"• Калории:  {120 * ratio:.0f} ккал\n"
        f"• Белки:    {10 * ratio:.1f} г\n"
        f"• Жиры:     {5 * ratio:.1f} г\n"
        f"• Углеводы: {15 * ratio:.1f} г"
    )


@tool
def adjust_diet_for_goal(goal: str, current_calories: float) -> str:
    """Скорректировать суточный рацион под цель. goal — цель (похудение/набор массы/поддержание), current_calories — текущее потребление ккал/день."""
    goal_lower = goal.lower()
    if any(w in goal_lower for w in ["похудение", "похудеть", "сбросить", "lose"]):
        target = max(1200, current_calories - 500)
        return (
            f"Цель: похудение\n"
            f"Текущее потребление: {current_calories:.0f} ккал/день\n"
            f"Рекомендуемая норма: {target:.0f} ккал/день (дефицит 500 ккал)\n"
            f"• Белки:    {target * 0.35 / 4:.0f} г/день (35%)\n"
            f"• Жиры:     {target * 0.25 / 9:.0f} г/день (25%)\n"
            f"• Углеводы: {target * 0.40 / 4:.0f} г/день (40%)\n"
            f"Советы: больше овощей, нежирный белок, убрать сахар и фастфуд."
        )
    elif any(w in goal_lower for w in ["набор", "набрать", "масса", "gain"]):
        target = current_calories + 400
        return (
            f"Цель: набор мышечной массы\n"
            f"Текущее потребление: {current_calories:.0f} ккал/день\n"
            f"Рекомендуемая норма: {target:.0f} ккал/день (профицит 400 ккал)\n"
            f"• Белки:    {target * 0.30 / 4:.0f} г/день (30%)\n"
            f"• Жиры:     {target * 0.25 / 9:.0f} г/день (25%)\n"
            f"• Углеводы: {target * 0.45 / 4:.0f} г/день (45%)\n"
            f"Советы: 5–6 приёмов пищи, протеин после тренировки, сложные углеводы."
        )
    else:
        return (
            f"Цель: поддержание веса\n"
            f"Текущее потребление: {current_calories:.0f} ккал/день\n"
            f"Рекомендуемая норма: {current_calories:.0f} ккал/день\n"
            f"• Белки:    {current_calories * 0.25 / 4:.0f} г/день\n"
            f"• Жиры:     {current_calories * 0.30 / 9:.0f} г/день\n"
            f"• Углеводы: {current_calories * 0.45 / 4:.0f} г/день\n"
            f"Советы: разнообразный рацион, 3 основных приёма пищи + 2 перекуса."
        )


