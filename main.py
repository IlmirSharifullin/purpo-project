@tool
def create_weekly_grocery_list(meals_plan: str) -> str:
    """Составить список покупок на неделю по плану питания. meals_plan — блюда через запятую."""
    meat = ["Куриное филе 1 кг", "Фарш говяжий 500 г"]
    dairy = ["Яйца 10 шт", "Молоко 1 л", "Творог 400 г", "Сыр 200 г"]
    veggies = ["Картофель 1 кг", "Морковь 500 г", "Лук репчатый 500 г", "Помидоры 400 г", "Огурцы 300 г"]
    grains = ["Гречка 500 г", "Рис 500 г", "Овсянка 400 г", "Макароны 400 г"]
    other = ["Масло растительное 0.5 л", "Масло сливочное 200 г", "Соль, перец, специи"]

    meals_lower = meals_plan.lower()
    if "рыба" in meals_lower or "лосось" in meals_lower:
        meat.append("Филе лосося 600 г")
    if "творог" in meals_lower or "запеканка" in meals_lower:
        dairy.append("Сметана 200 г")

    return (
        f"Список покупок на неделю (на основе: {meals_plan}):\n\n"
        "Мясо и рыба:\n" + "\n".join(f"  • {i}" for i in meat) + "\n\n"
        "Молочные продукты:\n" + "\n".join(f"  • {i}" for i in dairy) + "\n\n"
        "Овощи:\n" + "\n".join(f"  • {i}" for i in veggies) + "\n\n"
        "Крупы и злаки:\n" + "\n".join(f"  • {i}" for i in grains) + "\n\n"
        "Прочее:\n" + "\n".join(f"  • {i}" for i in other)
    )


